import turtle
import turtle as t

t.width(2)

turtle.delay(150)

# start drawing the rectangle
t.forward(300)
t.left(90)

t.forward(200)
t.left(90)

t.forward(300)
t.left(90)

t.forward(200)
t.left(90)


# hide the turtle pen
t.hideturtle()

