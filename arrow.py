import turtle
import turtle as t

t.width(2)
t.color('red')

turtle.delay(150)

t.begin_fill()

t.fd(200)
t.right(90)

t.fd(100)
t.left(120)
t.fd(150)

t.left(120)
t.fd(150)
t.left(120)

t.fd(100)
t.right(90)
t.fd(200)

t.right(90)
t.fd(50)

t.end_fill()

t.hideturtle()




