


import turtle
import turtle as t

t.width(2)

# draw first circle
t.color("white")
t.pencolor("black")

t.begin_fill()

t.circle(100)

t.end_fill()

t.up()
t.fd(150)
t.down()

#draw second circle
t.color('red')
t.begin_fill()

t.circle(100)

t.end_fill()



t.up()
t.fd(150)
t.down()

#draw third circle
t.color("white")
t.pencolor("black")

t.begin_fill()

t.circle(100)

t.end_fill()


